#include "iostream"
using namespace std;
int main(){
	string s;
	int n=1;
	while(cin>>s){
	   cout<<"Case "<<n<<": ";
	   if(s=="I")
	   cout<<1<<endl;
	   else if(s=="II")
	   cout<<2<<endl;
	   else if(s=="III")
	   cout<<3<<endl;
	   else if(s=="IV")
	   cout<<4<<endl;
	   else if(s=="V")
	   cout<<5<<endl;
	   else if(s=="VI")
	   cout<<6<<endl;
	   else if(s=="VII")
	   cout<<7<<endl;
	   else if(s=="VIII")
	   cout<<8<<endl;
	   else if(s=="IX")
	   cout<<9<<endl;
	   else if(s=="X")
	   cout<<10<<endl;
	   else if(s=="XI")
	   cout<<11<<endl;
	   else if(s=="XII")
	   cout<<12<<endl;
	   n++;
	}
	return 0; 
}
